local march = function(id)
// Stop one point before the end because voxel includes neighbouring points
    if id.x >= gridSize.x or id.y >= gridSize.y or id.z >= gridSize.z then
        return nil
    end

    // 8 corners of the current cube
    local cubeCorners = {
        {id, points[id.x][id.y][id.z]},
        {id + Vector(1, 0, 0), points[id.x + 1][id.y][id.z]},
        {id + Vector(1, 0, 1), points[id.x + 1][id.y][id.z + 1]},
        {id + Vector(0, 0, 1), points[id.x][id.y][id.z + 1]},
        {id + Vector(0, 1, 0), points[id.x][id.y + 1][id.z]},
        {id + Vector(1, 1, 0), points[id.x + 1][id.y + 1][id.z]},
        {id + Vector(1, 1, 1), points[id.x + 1][id.y + 1][id.z + 1]},
        {id + Vector(0, 1, 1), points[id.x][id.y + 1][id.z + 1]}
    }

    // Calculate unique index for each cube configuration.
    // There are 256 possible values
    // A value of 0 means cube is entirely inside surface; 255 entirely outside.
    // The value is used to look up the edge table, which indicates which edges of the cube are cut by the isosurface.
    local cubeIndex = 0
    if cubeCorners[1][2] < isoLevel then cubeIndex = bit.bor(1, cubeIndex) end
    if cubeCorners[2][2] < isoLevel then cubeIndex = bit.bor(2, cubeIndex) end
    if cubeCorners[3][2] < isoLevel then cubeIndex = bit.bor(4, cubeIndex) end
    if cubeCorners[4][2] < isoLevel then cubeIndex = bit.bor(8, cubeIndex) end
    if cubeCorners[5][2] < isoLevel then cubeIndex = bit.bor(16, cubeIndex) end
    if cubeCorners[6][2] < isoLevel then cubeIndex = bit.bor(32, cubeIndex) end
    if cubeCorners[7][2] < isoLevel then cubeIndex = bit.bor(64, cubeIndex) end
    if cubeCorners[8][2] < isoLevel then cubeIndex = bit.bor(128, cubeIndex) end

    // Create triangles for current cube configuration
    local vertices = {}
    local i = 0
    while triangulation[cubeIndex + 1][i + 1] != -1 do
        // Get indices of corner points A and B for each of the three edges
        // of the cube that need to be joined to form the triangle.
        local a0 = cornerIndexAFromEdge[triangulation[cubeIndex + 1][i + 1] + 1];
        local b0 = cornerIndexBFromEdge[triangulation[cubeIndex + 1][i + 1] + 1];

        local a1 = cornerIndexAFromEdge[triangulation[cubeIndex + 1][i + 2] + 1];
        local b1 = cornerIndexBFromEdge[triangulation[cubeIndex + 1][i + 2] + 1];

        local a2 = cornerIndexAFromEdge[triangulation[cubeIndex + 1][i + 3] + 1];
        local b2 = cornerIndexBFromEdge[triangulation[cubeIndex + 1][i + 3] + 1];

        --[[table.insert(tris, {
            {pos = interpolateVerts(cubeCorners[a0 + 1][1], cubeCorners[b0 + 1][1]) * Vector(worldSize)},
            {pos = interpolateVerts(cubeCorners[a1 + 1][1], cubeCorners[b1 + 1][1]) * Vector(worldSize)},
            {pos = interpolateVerts(cubeCorners[a2 + 1][1], cubeCorners[b2 + 1][1]) * Vector(worldSize)}
        })]]
        --((b-a):cross(c-a)):getNormalized()
        local v1 = interpolateVerts(cubeCorners[a0 + 1][1], cubeCorners[b0 + 1][1]) * scaleMult - Vector(0, 0, 1)
        local v2 = interpolateVerts(cubeCorners[a1 + 1][1], cubeCorners[b1 + 1][1]) * scaleMult - Vector(0, 0, 1)
        local v3 = interpolateVerts(cubeCorners[a2 + 1][1], cubeCorners[b2 + 1][1]) * scaleMult - Vector(0, 0, 1)
        --[[table.insert(vertices, v1)
        table.insert(vertices, v2)
        table.insert(vertices, v3)]]
        table.insert(tris, {pos = v1})
        table.insert(tris, {pos = v2})
        table.insert(tris, {pos = v3})
        
        --[[local vertices = {
            interpolateVerts(cubeCorners[a0 + 1][1], cubeCorners[b0 + 1][1]) * Vector(worldSize),
            interpolateVerts(cubeCorners[a1 + 1][1], cubeCorners[b1 + 1][1]) * Vector(worldSize),
            interpolateVerts(cubeCorners[a2 + 1][1], cubeCorners[b2 + 1][1]) * Vector(worldSize)
        }
        local normal = ((vertices[2] - vertices[1]):cross(vertices[3] - vertices[1])):normalize()
        table.insert(tris, {pos = vertices[1], normal = normal})
        table.insert(tris, {pos = vertices[2], normal = normal})
