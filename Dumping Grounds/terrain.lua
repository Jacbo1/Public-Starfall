local interpolateVerts = function(v1, v2)
    --print(v1 .. " | " .. v2)
    local t = (isoLevel - points[v1.x][v1.y][v1.z]) / (points[v2.x][v2.y][v2.z] - points[v1.x][v1.y][v1.z])
    --print(t)
    return v1 + Vector(t) * (v2-v1)
    --return (v1 + v2) / Vector(2)
end
