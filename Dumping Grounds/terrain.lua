local taylorInvSqrt = function(r)
    return 1.79284291400159 - r * 0.85373472095314
end
--Get value at point
local snoise = function(v)
    local C = Vector(1 / 6, 1 / 3, 0)

    // First corner
    local i  = v + Vector(v:dot(C.yyy))
    i = Vector(math.floor(i.x), math.floor(i.y), math.floor(i.z))
    local x0 = v - i + Vector(i:dot(C.xxx))

    // Other corners
    --local g = Vector(x0.yzx, x0.xyz);
    local g = Vector(
        x0.y > x0.x and 0 or 1,
        x0.z > x0.y and 0 or 1,
        x0.x > x0.z and 0 or 1
    )
    local l = Vector(1) - g
    local i1 = Vector(
        math.min(g.x, l.z),
        math.min(g.y, l.x),
        math.min(g.z, l.y)
    )
    local i2 = Vector(
        math.max(g.x, l.z),
        math.max(g.y, l.x),
        math.max(g.z, l.y)
    )

    // x1 = x0 - i1  + 1.0 * C.xxx;
    // x2 = x0 - i2  + 2.0 * C.xxx;
    // x3 = x0 - 1.0 + 3.0 * C.xxx;
    local x1 = x0 - i1 + C.xxx
    local x2 = x0 - i2 + C.yyy
    local x3 = x0 - Vector(0.5)

    // Permutations
    i = Vector(mod289(i.x), mod289(i.y), mod289(i.z))  // Avoid truncation effects in permutation
    local p = {
        permute(permute(permute(i.z) + i.y) + i.x),
        permute(permute(permute(i.z + i1.z) + i.y + i1.y) + i.x + i1.x),
        permute(permute(permute(i.z + i2.z) + i.y + i2.y) + i.x + i2.x),
        permute(permute(permute(i.z + 1) + i.y + 1) + i.x + 1)
    }
    --local p = permute(permute(permute(i.z + Vector(0.0, i1.z, i2.z, 1.0)) + i.y + Vector(0.0, i1.y, i2.y, 1.0)) + i.x + Vector(0.0, i1.x, i2.x, 1.0));

    // Gradients: 7x7 points over a square, mapped onto an octahedron.
    // The ring size 17*17 = 289 is close to a multiple of 49 (49*6 = 294)
    --local j = p - 49.0 * floor(p / 49.0);  // mod(p,7*7)
    local j = {}
    local x_ = {}
    local y_ = {}
    local x = {}
    local y = {}
    local h = {}
    for abc = 1, 4 do
        table.insert(j, p[abc] - 49 * math.floor(p[abc] / 49))  // mod(p,7*7)
        table.insert(x_, math.floor(j[abc] / 7))
        table.insert(y_, math.floor(j[abc] - 7 * x_[abc]))  // mod(j,N)

        table.insert(x, (x_[abc] * 2 + 0.5) / 7 - 1)
        table.insert(y, (y_[abc] * 2 + 0.5) / 7 - 1)

        table.insert(h, 1 - math.abs(x[abc]) - math.abs(y[abc]))
    end
    local b0 = {x[1], x[2], y[1], y[2]}
    local b1 = {x[3], x[4], y[3], y[4]}

    //float4 s0 = float4(lessThan(b0, 0.0)) * 2.0 - 1.0;
    //float4 s1 = float4(lessThan(b1, 0.0)) * 2.0 - 1.0;
    local s0 = {}
    local s1 = {}
    local sh = {}
    for abc = 1, 4 do
        table.insert(s0, math.floor(b0[abc]) * 2 + 1)
    
        table.insert(s1, math.floor(b1[abc]) * 2 + 1)
        table.insert(sh, h[abc] > 0 and 0 or -1)
    end
    
    --local a0 = Vector(b0.xzyw + s0.xzyw * sh.xxyy
    local a0 = {
        b0[1] + s0[1] * sh[1],
        b0[3] + s0[3] * sh[1],
        b0[2] + s0[2] * sh[2],
        b0[4] + s0[4] * sh[2]
    }
    --local a1 = b1.xzyw + s1.xzyw * sh.zzww
    local a1 = {
        b1[1] + s1[1] * sh[3],
        b1[3] + s1[3] * sh[3],
        b1[2] + s1[2] * sh[4],
        b1[4] + s1[4] * sh[4]
    }

    --[[local g0 = float3(a0.xy, h.x);
    local g1 = float3(a0.zw, h.y);
    local g2 = float3(a1.xy, h.z);
    local g3 = float3(a1.zw, h.w);]]

    local g0 = Vector(a0[1], a0[2], h[1])
    local g1 = Vector(a0[3], a0[4], h[2])
    local g2 = Vector(a1[1], a1[2], h[3])
    local g3 = Vector(a1[3], a1[4], h[4])
    // Normalise gradients
    --local norm = taylorInvSqrt(float4(dot(g0, g0), dot(g1, g1), dot(g2, g2), dot(g3, g3)));
    local norm = {
        taylorInvSqrt(g0:dot(g0)),
        taylorInvSqrt(g1:dot(g1)),
        taylorInvSqrt(g2:dot(g2)),
        taylorInvSqrt(g3:dot(g3))
    }
    g0 = g0 * Vector(norm[1])
    g1 = g1 * Vector(norm[2])
    g2 = g2 * Vector(norm[3])
    g3 = g3 * Vector(norm[4])

    // Mix final noise value
    --local m = max(0.6 - float4(dot(x0, x0), dot(x1, x1), dot(x2, x2), dot(x3, x3)), 0.0);
    local m = {
        math.max(0.6 - x0:dot(x0), 0),
        math.max(0.6 - x1:dot(x1), 0),
        math.max(0.6 - x2:dot(x2), 0),
        math.max(0.6 - x3:dot(x3), 0)
    }
    for abc = 1, 4 do
        m[abc] = m[abc] ^ 3
    end

    --local px = float4(dot(x0, g0), dot(x1, g1), dot(x2, g2), dot(x3, g3));
    local px = {
        x0:dot(g0),
        x1:dot(g1),
        x2:dot(g2),
        x3:dot(g3)
    }
    --return 42.0 * m:dot(px);
    --return a.x*b.x + a.y*b.y + a.z*b.z + a.w*b.w;
    return 42 * m[1] * px[1] + m[2] * px[2] + m[3] * px[3] + m[4] * px[4] + 2
end

